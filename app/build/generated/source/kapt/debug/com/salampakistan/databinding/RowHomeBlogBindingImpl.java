    protected void executeBindings() {
        long dirtyFlags = 0;
        synchronized(this) {
            dirtyFlags = mDirtyFlags;
            mDirtyFlags = 0;
        }
        com.salampakistan.model.Blog model = mModel;
        java.lang.String modelImagePath = null;
        java.lang.String modelTitle = null;
        java.lang.String modelFeaturedText = null;

        if ((dirtyFlags & 0x3L) != 0) {



                if (model != null) {
                    // read model.imagePath
                    modelImagePath = model.getImagePath();
                    // read model.title
                    modelTitle = model.getTitle();
                    // read model.featuredText
                    modelFeaturedText = model.getFeaturedText();
                }
        }
        // batch finished
        if ((dirtyFlags & 0x3L) != 0) {
            // api target 1

            androidx.databinding.adapters.TextViewBindingAdapter.setText(this.descText, modelFeaturedText);
            com.salampakistan.utils.BindingAdaptersKt.setImageUrl(this.mboundView2, modelImagePath);
            androidx.databinding.adapters.TextViewBindingAdapter.setText(this.titleText, modelTitle);
        }
    }
